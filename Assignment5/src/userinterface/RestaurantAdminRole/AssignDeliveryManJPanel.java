    private void populateDeliveryPersonTable() {
        
        DefaultTableModel model = (DefaultTableModel) tblDeliveryPerson.getModel();
        
        model.setRowCount(0);
        
        for(DeliveryMan dm:system.getDeliveryManDirectory().getDeliveryManList()){
            
               Object[] row = new Object[1];
                row[0] = dm;
                model.addRow(row);
        }
    }
